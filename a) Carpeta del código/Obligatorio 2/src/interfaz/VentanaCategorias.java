    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String desc = txtDescripcion.getText();
        int prio = comBoxPrioridad.getSelectedIndex() + 1;
        String detall = txtDetalles.getText();

        if(!desc.replaceAll(" ", "").equals("")){
            detall = detall.replace("\r\n", "_"); //cambia los saltos de linea por "_" (para facilitar uso proximamente)
            
            Categoria categoria = new Categoria(desc, detall, prio);
            
            if(modelo.comprobarExisteCategoria(categoria)){
                modelo.addCategoria(categoria);

                txtDescripcion.setText("");
                txtDetalles.setText("");

                JOptionPane.showMessageDialog(this, "La categoria se ha ingresado correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                modelo.imprimirCategorias();
                lstCegsAgregadas.setListData(modelo.getListaCategoriasNombres().toArray());
            }
            else{
                JOptionPane.showMessageDialog(this, "La categoria YA EXISTE,\nNO se ha podido ingresar", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "La categoria NO se ha podido ingresar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


